# STRING
name = "Dimitar"
print(type(name))
print("Hello" + name)

# CONCATINATION
first_name = "Dimitar"
last_name = "Mitev"
full_name = first_name + " " + last_name
print(full_name)

# INTEGER
age = 32 
age = age + 1
age += 1
print(age)
print(type(age))
print("Your age is " + str(age))

# FLOAT
height = 250.5
print("Your height is " + str(height) + "cm")
print(type(height))

# BOOLEAN
human = True
print(type(human))
print("Are you a human: " + str(human))
